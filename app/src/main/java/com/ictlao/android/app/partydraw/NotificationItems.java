package com.ictlao.android.app.partydraw;

public class NotificationItems {

    private String G;
    private String P;
    private String T;
    private String N;

    public  NotificationItems( String g, String p, String t, String n){
        this.G = g;
        this.P = p;
        this.T = t;
        this.N = n;
    }

    public String getG(){
        return  G;
    }

    public String getP(){
        return P;
    }

    public String getT(){
        return T;
    }

    public String getN(){
        return N;
    }
}
